"""Pida al usuario el espacio recorrido por un coche y el tiempo que ha tardado 
en horas y que calcule a qué velocidad media había realizado el recorrido."""

print ("Para calcular la velocidad nos falta saber la distancia y el tiempo ")

distancia=int(input("indica por favor la distancia recorrida en kilometros: "))

tiempo=int(input("indica por favor el tiempo que tarda en horas: "))

velocidad=distancia/tiempo

print ("la velocidad es ",velocidad," km/h")


